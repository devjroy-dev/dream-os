-- §3. INDEXES — including PARTIAL indexes (F-04.32's block guarantee lives here)
-- ════════════════════════════════════════════════════════════════════
select
  (select count(*)
     from pg_indexes
    where schemaname = 'public')                           as rows_expected,
  tablename                                                as table_name,
  indexname                                                as index_name,
  indexdef                                                 as definition
from pg_indexes
where schemaname = 'public'
order by tablename, indexname;
-- ⚠ THE `WHERE` CLAUSE OF A PARTIAL INDEX IS INSIDE `indexdef` AND NOWHERE ELSE.
--   This is the whole reason this section exists rather than being folded into §1.
--   F-04.32's block guarantee is a UNIQUE PARTIAL index — unique on (vendor_id,
--   event_date) WHERE kind = 'blocked' AND deleted_at IS NULL, per 0075's DDL. Read
--   without its predicate it looks like a unique index that would forbid a vendor
--   two events on one date, which would be catastrophic and false. **A constraint
--   read without its predicate is a constraint misread.**
--
-- EXPECTED: one unique partial index on public.events implementing the block
-- guarantee; a supporting index on (vendor_id, event_date). Predictions, to be
-- falsified by the output.
