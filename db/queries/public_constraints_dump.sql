-- §2. FOREIGN KEYS — with the referenced table and the delete rule
-- ════════════════════════════════════════════════════════════════════
select
  (select count(*)
     from pg_constraint pc
     join pg_class pcl on pcl.oid = pc.conrelid
     join pg_namespace pn on pn.oid = pcl.relnamespace
    where pn.nspname = 'public'
      and pcl.relkind = 'r'
      and pc.contype = 'f')                                as rows_expected,
  cl.relname                                               as table_name,
  con.conname                                              as constraint_name,
  pg_get_constraintdef(con.oid)                            as definition
from pg_constraint con
join pg_class cl      on cl.oid = con.conrelid
join pg_namespace ns  on ns.oid = cl.relnamespace
where ns.nspname = 'public'
  and cl.relkind = 'r'
  and con.contype = 'f'
order by cl.relname, con.conname;
-- `pg_get_constraintdef` carries the referenced table, the columns and the ON
-- DELETE rule in one string — which is why this section does not hand-join
-- key_column_usage to constraint_column_usage. That join is the standard recipe and
-- it is ALSO the recipe that silently drops composite keys by matching on
-- ordinal_position. One catalog function, one string, no join to get wrong.
--
-- EXPECTED: `events.linked_binder_id` carries NO FK — F-04.55's entry and the
-- checker's own comment both state that `entity_id` and the binder link are
-- informational only. **This section is where that claim gets witnessed instead of
-- repeated.**


