-- §1. CHECK / UNIQUE / PRIMARY KEY CONSTRAINTS
-- ════════════════════════════════════════════════════════════════════
select
  -- THE SELF-COMPUTING GUARD. A scalar subquery: computed by the database, repeated
  -- on every row, structurally immune to the cap that truncates the set around it.
  (select count(*)
     from pg_constraint pc
     join pg_class pcl on pcl.oid = pc.conrelid
     join pg_namespace pn on pn.oid = pcl.relnamespace
    where pn.nspname = 'public'
      and pcl.relkind = 'r'
      and pc.contype in ('c', 'u', 'p'))                   as rows_expected,
  cl.relname                                               as table_name,
  con.conname                                              as constraint_name,
  case con.contype when 'c' then 'CHECK'
                   when 'u' then 'UNIQUE'
                   when 'p' then 'PRIMARY KEY' end         as constraint_type,
  pg_get_constraintdef(con.oid)                            as definition
from pg_constraint con
join pg_class cl      on cl.oid = con.conrelid
join pg_namespace ns  on ns.oid = cl.relnamespace
where ns.nspname = 'public'
  and cl.relkind = 'r'                                     -- BASE TABLEs only
  and con.contype in ('c', 'u', 'p')
order by cl.relname, con.contype, con.conname;
-- EXPECTED, at the time of writing (2026-07-16, dream-os 5cdf356) — stated as
-- PREDICTIONS TO BE FALSIFIED BY THE OUTPUT, never as numbers to trust:
--   · public.events carries a CHECK on `slot` reading
--     `slot in ('morning','noon','evening','full_day')` — 0075:181's DDL.
--   · public.vendors.slot_capacity carries NO CHECK. Q-SP-1 ruled 0 LAWFUL and
--     negative values UNRULED; 0076 added the column bare. **If a CHECK appears
--     here, a ruling has been overtaken by the database and the executor records it
--     as a finding rather than quietly adjusting this comment.**
--   · IF ANY OF THIS IS WRONG, THE DATABASE IS RIGHT AND THIS COMMENT IS WRONG.


